namespace Sales.Models.Excel
{
    public class VendorReportRecord
    {
        public string Name { get; set; }

        public decimal Incomes { get; set; }

        public decimal Expenses { get; set; }

        public decimal Taxes { get; set; }

        public decimal FinancialResults { get; set; }

        public override string ToString()
        {
            return string.Format("Name: {0}, Incomes: {1}, Expenses: {2}, Taxes: {3}, FinancialResults: {4}", Name, Incomes, Expenses, Taxes, FinancialResults);
        }
    }
}