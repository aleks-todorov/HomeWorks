function OpenPopupWindow() {
    document.getElementById('pnlPopup').style.display = 'block';
    document.getElementById('pnlFade').style.display = 'block';
}
function ClosePopupWindow() {
    document.getElementById('pnlPopup').style.display = 'none';
    document.getElementById('pnlFade').style.display = 'none';
}